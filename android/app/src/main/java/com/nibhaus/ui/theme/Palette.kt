    val DEFAULT: Palette = byId("D01")
    val light: List<Palette> = ALL.filter { !it.isDark }
    val dark: List<Palette> = ALL.filter { it.isDark }

    /** Id for the Material You "Match my wallpaper" entry (design-system §12 add-on, Android 12+).
     *  Deliberately NOT in [ALL] — its real tokens depend on a runtime `Context` + the current
     *  system dark/light mode, so they can't be baked in statically like the other 10. See
     *  [dynamicPalette], which [com.nibhaus.ui.NibhausTheme] calls to resolve the real thing. */
    const val DYNAMIC_ID = "DYN"

    /** Stand-in for [DYNAMIC_ID] before a live Context resolves it (e.g. the picker's active/pending
     *  id comparisons, or a stray direct render pre-resolution) — same tokens as [DEFAULT] so it's
     *  never actually blank/broken, just not yet personalized. */
    val DYNAMIC_PLACEHOLDER: Palette = DEFAULT.copy(id = DYNAMIC_ID, name = "Match my wallpaper")

    fun byId(id: String?): Palette =
        ALL.firstOrNull { it.id == id } ?: if (id == DYNAMIC_ID) DYNAMIC_PLACEHOLDER else DEFAULT
}
